#include "libft.h"

size_t	ft_ilen(long int nb)
{
	size_t	i;

	if (nb < 0)
	{
		i = 2;
		nb *= -1;
	}
	else
		i = 1;
	while (nb / 10)
	{
		nb /= 10;
		++i;
	}
	return (i);
}
